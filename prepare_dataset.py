"""
Generate dataset.yaml for YOLO training + verify label structure.

Expected directory layout (created by LabelStudio CVAT export → YOLO 1.1 format):

    dataset_frames/
    ├── images/
    │   ├── train/
    │   │   ├── frame_000001.jpg
    │   │   └── ...
    │   ├── val/
    │   └── test/
    ├── labels/
    │   ├── train/
    │   │   ├── frame_000001.txt   (YOLO format: class_id x_center y_center width height)
    │   │   └── ...
    │   ├── val/
    │   └── test/
    └── dataset.yaml               (auto-generated by this script)

Usage:
    python prepare_dataset.py --images-dir ./dataset_frames/images \\
                              --labels-dir ./dataset_frames/labels \\
                              --classes dent scratch crack broken_glass ...
"""

import argparse
from pathlib import Path


def generate_dataset_yaml(
    images_dir: str,
    labels_dir: str,
    classes: list[str],
    output_yaml: str = "dataset.yaml",
):
    images_dir = Path(images_dir)
    labels_dir = Path(labels_dir)

    # Validate structure
    for split in ["train", "val", "test"]:
        img_split = images_dir / split
        lbl_split = labels_dir / split
        if not img_split.exists():
            print(f"  ! {split}/ images dir missing: {img_split.resolve()}")
        if not lbl_split.exists():
            print(f"  ! {split}/ labels dir missing: {lbl_split.resolve()}")

    content = f"""# YOLO Dataset — Insurance Damage
# Auto-generated by prepare_dataset.py

path: {images_dir.parent.resolve()}   # dataset root
train: images/train
val: images/val
test: images/test

nc: {len(classes)}
names: {classes}
"""
    out_path = Path(output_yaml)
    out_path.write_text(content)
    print(f"✓ {out_path.resolve()}")
    print(f"  {len(classes)} classes: {classes}")

    # Quick validation: count mismatched image/label pairs
    for split in ["train", "val", "test"]:
        img_dir = images_dir / split
        lbl_dir = labels_dir / split
        if not img_dir.exists() or not lbl_dir.exists():
            continue
        imgs = {p.stem for p in img_dir.glob("*.jpg")}
        lbls = {p.stem for p in lbl_dir.glob("*.txt")}
        missing_labels = imgs - lbls
        missing_images = lbls - imgs
        if missing_labels:
            print(f"  ! {split}: {len(missing_labels)} images without labels"
                  f" (e.g. {sorted(missing_labels)[:3]})")
        if missing_images:
            print(f"  ! {split}: {len(missing_images)} labels without images"
                  f" (e.g. {sorted(missing_images)[:3]})")
        if not missing_labels and not missing_images:
            print(f"  ✓ {split}: {len(imgs)} images, {len(lbls)} labels — matched")


if __name__ == "__main__":
    parser = argparse.ArgumentParser()
    parser.add_argument("--images-dir", default="dataset_frames/images",
                        help="path to images/ dir with train/val/test subdirs")
    parser.add_argument("--labels-dir", default="dataset_frames/labels",
                        help="path to labels/ dir with train/val/test subdirs")
    parser.add_argument("--output", default="dataset.yaml",
                        help="output dataset.yaml path")
    parser.add_argument("--classes", nargs="+", required=True,
                        help="class names in order matching class_id")
    args = parser.parse_args()
    generate_dataset_yaml(args.images_dir, args.labels_dir, args.classes, args.output)
